// Merge in premium tactics for maps that don't have them inline (hand-written
// strats). For each (mapId, siteId, side) in PREMIUM_TACTICS, attach the
// premium tactics object IF the strat exists AND doesn't already have one.
// Additive — won't clobber generated maps' inline premiumTactics.
for (const [mapId, sites] of Object.entries(PREMIUM_TACTICS)) {
  const map = STRATS[mapId]
  if (!map) continue
  for (const [siteId, premium] of Object.entries(sites)) {
    const site = map[siteId]
    if (!site) continue
    if (site.attack && !site.attack.premiumTactics && premium.attack) {
      site.attack.premiumTactics = premium.attack
    }
    if (site.defense && !site.defense.premiumTactics && premium.defense) {
      site.defense.premiumTactics = premium.defense
    }
  }
}

